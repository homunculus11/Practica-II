public class Curs {
    final int id;
    final String title;
    final String type;
    final double price;
    final String coordinator;

    Curs(int id, String title, String type, double price, String coordinator) {
        this.id = id;
        this.title = title;
        this.type = type;
        this.price = price;
        this.coordinator = coordinator;
    }
}

